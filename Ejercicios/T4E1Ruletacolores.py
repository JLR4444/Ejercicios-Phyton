color = input("Elige un color (Rojo, Verde, Azul, Amarillo, Morado): ")
if color == "Rojo":
    print("Pasión y energía")
elif color == "Verde":
    print("Esperanza y crecimiento")
elif color == "Azul":
    print("Calma y serenidad")
elif color == "Amarillo":
    print("Felicidad y optimismo")
elif color == "Morado":
    print("Sabiduría y creatividad")
else:
    print("Color no válido")